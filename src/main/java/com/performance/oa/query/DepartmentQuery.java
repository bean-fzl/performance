package com.ync365.oa.query;

import com.ync365.oa.entity.Employe;

public class DepartmentQuery {
	private Long id;
	private String departmentName;
	private String mt;
	private String eployeName;
	private Integer type;
	private Employe e;
	private Integer pageIndex;
	private Integer pageSize;
	private String sort;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	public String getMt() {
		return mt;
	}
	public void setMt(String mt) {
		this.mt = mt;
	}
	public String getEployeName() {
		return eployeName;
	}
	public void setEployeName(String eployeName) {
		this.eployeName = eployeName;
	}
	public Integer getPageIndex() {
		return pageIndex;
	}
	public void setPageIndex(Integer pageIndex) {
		this.pageIndex = pageIndex;
	}
	public Integer getPageSize() {
		return pageSize;
	}
	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public Employe getE() {
		return e;
	}
	public void setE(Employe e) {
		this.e = e;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	
}
