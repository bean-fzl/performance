package com.ync365.oa.query;

import java.util.Date;

public class EmployeQuery {
	public  static final Integer AVAILABLE=0;//可以查看的员工没有被删除的
	private Long id;
	private String name;
	private String code;
	private String departmentName;
	private Boolean isMt;
	private Boolean isPm;
	private Date createStartTime;
	private Date createEndTime;
	private String position;
	private Integer pageIndex;
	private Integer pageSize;
	private String sort;
	private Integer isDel;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	public Boolean getIsMt() {
		return isMt;
	}
	public void setIsMt(Boolean isMt) {
		this.isMt = isMt;
	}
	public Boolean getIsPm() {
		return isPm;
	}
	public void setIsPm(Boolean isPm) {
		this.isPm = isPm;
	}
	public Date getCreateStartTime() {
		return createStartTime;
	}
	public void setCreateStartTime(Date createStartTime) {
		this.createStartTime = createStartTime;
	}
	public Date getCreateEndTime() {
		return createEndTime;
	}
	public void setCreateEndTime(Date createEndTime) {
		this.createEndTime = createEndTime;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public Integer getPageIndex() {
		return pageIndex;
	}
	public void setPageIndex(Integer pageIndex) {
		this.pageIndex = pageIndex;
	}
	public Integer getPageSize() {
		return pageSize;
	}
	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public Integer getIsDel() {
		return isDel;
	}
	public void setIsDel(Integer isDel) {
		this.isDel = isDel;
	}
	
}
