package com.ync365.oa.query;

import java.util.Date;

public class ProjectQuery {
	private Long id;
	private String name;
	private String projectEmploye;
	private String pm;
	private Date projectBeginTime;
	private Date projectEndTime;
	private Integer state;
	private String projectPersonnel;
	private Integer pageIndex;
	private Integer pageSize;
	private String sort;
	
	
	private Integer planHoursSearch;//搜索条件 计划工时
	private Integer actualHoursSearch;//搜索条件 实际工时
	private Integer outputHoursSearch;//搜索条件 产出工时
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	 
	public String getProjectEmploye() {
		return projectEmploye;
	}
	public void setProjectEmploye(String projectEmploye) {
		this.projectEmploye = projectEmploye;
	}
	public String getPm() {
		return pm;
	}
	public void setPm(String pm) {
		this.pm = pm;
	}
	public Date getProjectBeginTime() {
		return projectBeginTime;
	}
	public void setProjectBeginTime(Date projectBeginTime) {
		this.projectBeginTime = projectBeginTime;
	}
	public Date getProjectEndTime() {
		return projectEndTime;
	}
	public void setProjectEndTime(Date projectEndTime) {
		this.projectEndTime = projectEndTime;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public String getProjectPersonnel() {
		return projectPersonnel;
	}
	public void setProjectPersonnel(String projectPersonnel) {
		this.projectPersonnel = projectPersonnel;
	}
	public Integer getPageIndex() {
		return pageIndex;
	}
	public void setPageIndex(Integer pageIndex) {
		this.pageIndex = pageIndex;
	}
	public Integer getPageSize() {
		return pageSize;
	}
	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
    public Integer getPlanHoursSearch() {
        return planHoursSearch;
    }
    public void setPlanHoursSearch(Integer planHoursSearch) {
        this.planHoursSearch = planHoursSearch;
    }
    public Integer getActualHoursSearch() {
        return actualHoursSearch;
    }
    public void setActualHoursSearch(Integer actualHoursSearch) {
        this.actualHoursSearch = actualHoursSearch;
    }
    public Integer getOutputHoursSearch() {
        return outputHoursSearch;
    }
    public void setOutputHoursSearch(Integer outputHoursSearch) {
        this.outputHoursSearch = outputHoursSearch;
    }
	
	
}
